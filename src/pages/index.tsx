import Head from "next/head";
import Link from "next/link";
import { api } from "@/utils/api";
import { useState } from "react";
import TweetPost from "@/components/post/tweet";
import { toast } from "react-hot-toast";
import { PageLayout } from "@/components/layout";

export default function Home() {
  const { mutateAsync, data } = api.threadslogin.login.useMutation();
  const { mutateAsync: post } = api.threadslogin.post.useMutation();
  const [username, setUsername] = useState("");
  const [password, setPassword] = useState("");

  const onsubmit = () => {
    mutateAsync({ username, password }).then(() => {
      alert(data?.token);
      if (data) {
        const token = data.token;
        if (token) {
          localStorage.setItem("token", token);
          localStorage.setItem("username", username);
        }
      }
    });
  };

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <PageLayout>
      <TweetPost/>
      <main className="flex h-screen w-full items-center justify-center gap-2">
        <input
          onChange={(e) => {
            setUsername(e.target.value);
          }}
          type="text"
          placeholder="username"
          className="rounded-md border"
        />
        <input
          onChange={(e) => {
            setPassword(e.target.value);
          }}
          type="password"
          placeholder="password"
          className="rounded-md border"
        />
        <button onClick={onsubmit} className="rounded bg-blue-400 px-6 py-3">
          Submit
        </button>
        <button
          onClick={() => {
            const token = localStorage.getItem("token");
            const username = localStorage.getItem("username");
            if (token && username ) {
              post({ token,username });
            }else{
              toast.error("please login again")
            }
          }}
        >
          post
        </button>
      </main>
      </PageLayout>
    </>
  );
}
